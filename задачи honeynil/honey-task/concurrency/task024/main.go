// Задача 4 – GOMAXPROCS(1) и бесконечный цикл
// Как отработает код?
//
// ОТВЕТ: Зависит от версии Go:
//
// Go < 1.14: программа зависнет НАВСЕГДА (никогда не напечатает "Hello world!")
//   - GOMAXPROCS(1): только один поток выполнения
//   - Горутина "Hello world!" поставлена в очередь, но main занимает поток бесконечным for {}
//   - Горутина с fmt.Println никогда не получит управление
//   - for {} не содержит точек preemption — кооперативная многозадачность не работает
//
// Go >= 1.14: горутина МОЖЕТ успеть выполниться (async preemption)
//   - Введён асинхронный preemption через сигналы SIGURG
//   - main будет прерван, горутина выведет "Hello world!"
//   - После этого main захватит поток обратно → вечный цикл
//
// На практике (Go 1.14+): "Hello world!" ВЫВОДИТСЯ, затем программа зависает в for {}
package main

import (
	"fmt"
	"runtime"
)

func main() {
	runtime.GOMAXPROCS(1)

	go func() { fmt.Println("Hello world!") }()

	for {
	}
}
