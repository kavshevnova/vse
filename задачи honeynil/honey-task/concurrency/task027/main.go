// Задача 7 – Указатели и «смена адреса»
// Как отработает код?
//
// ОТВЕТ:
//	5
//
// Объяснение:
// - p = &v в changePointer создаёт ЛОКАЛЬНУЮ переменную v со значением 3
// - p передаётся по значению (копия указателя)
// - p = &v внутри функции меняет ЛОКАЛЬНУЮ копию указателя, не исходный p в main
// - исходный p в main по-прежнему указывает на v=5
// - *p в main = 5 (не изменилось)
//
// ИСПРАВЛЕНИЕ для реального изменения: передать **int или вернуть новый указатель
package main

import "fmt"

func changePointer(p *int) {
	v := 3
	p = &v
}

func main() {
	v := 5
	p := &v
	changePointer(p)
	fmt.Println(*p) // 5
}
